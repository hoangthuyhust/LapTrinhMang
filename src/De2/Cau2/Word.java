package De2.Cau2;

/**
 *
 * @author Admin
 */
public class Word {
    public String tat;
    public String dayDu;

    public Word() {
    }

    public Word(String tat, String dayDu) {
        this.tat = tat;
        this.dayDu = dayDu;
    }

    public String getTat() {
        return tat;
    }

    public void setTat(String tat) {
        this.tat = tat;
    }

    public String getDayDu() {
        return dayDu;
    }

    public void setDayDu(String dayDu) {
        this.dayDu = dayDu;
    }
    
}

